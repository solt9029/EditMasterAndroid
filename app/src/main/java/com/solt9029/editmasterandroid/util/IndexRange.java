package com.solt9029.editmasterandroid.util;

public class IndexRange {
    private int first;
    private int last;

    public IndexRange(int first, int last) {
        this.first = first;
        this.last = last;
    }

    public int getFirst() {
        return first;
    }

    public void setFirst(int first) {
        this.first = first;
    }

    public int getLast() {
        return last;
    }

    public void setLast(int last) {
        this.last = last;
    }
}
